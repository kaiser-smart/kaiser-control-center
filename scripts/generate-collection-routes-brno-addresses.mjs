import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ADDRESS_LAYER_QUERY_URL = "https://gis.brno.cz/ags1/rest/services/Hosted/OD_adresni_mista_Brno/FeatureServer/0/query";
const ADDRESS_SEED = 20260712;
const TARGET_COUNT = 500;
const CANDIDATE_COUNT = 1400;
const QUERY_CHUNK_SIZE = 200;
const OUTPUT_PATH = path.resolve(
  path.dirname(fileURLToPath(import.meta.url)),
  "../functions/_data/collection-routes-test-brno-addresses.generated.js"
);

function mulberry32(seed) {
  let value = seed >>> 0;
  return () => {
    value += 0x6d2b79f5;
    let next = value;
    next = Math.imul(next ^ (next >>> 15), next | 1);
    next ^= next + Math.imul(next ^ (next >>> 7), next | 61);
    return ((next ^ (next >>> 14)) >>> 0) / 4294967296;
  };
}

function shuffled(values, seed) {
  const result = [...values];
  const random = mulberry32(seed);
  for (let index = result.length - 1; index > 0; index -= 1) {
    const target = Math.floor(random() * (index + 1));
    [result[index], result[target]] = [result[target], result[index]];
  }
  return result;
}

function chunks(values, size) {
  const result = [];
  for (let index = 0; index < values.length; index += size) {
    result.push(values.slice(index, index + size));
  }
  return result;
}

async function arcgisQuery(parameters) {
  const response = await fetch(ADDRESS_LAYER_QUERY_URL, {
    method: "POST",
    headers: { "Content-Type": "application/x-www-form-urlencoded" },
    body: new URLSearchParams({ ...parameters, f: "json" })
  });
  if (!response.ok) {
    throw new Error(`GIS Brno odpovědělo HTTP ${response.status}.`);
  }
  const payload = await response.json();
  if (payload?.error) {
    throw new Error(payload.error.message || "GIS Brno vrátilo chybu dotazu.");
  }
  return payload;
}

function cleanString(value) {
  return String(value ?? "").trim();
}

function normalizedAddress(feature) {
  const attributes = feature?.attributes || {};
  const street = cleanString(attributes.naz_ul_a);
  const descriptiveNumber = cleanString(attributes.cis_d);
  const orientationNumber = cleanString(attributes.cis_o);
  const city = cleanString(attributes.naz_obec);
  const latitude = Number(feature?.geometry?.y);
  const longitude = Number(feature?.geometry?.x);
  if (
    !street ||
    !descriptiveNumber ||
    city !== "Brno" ||
    !Number.isFinite(latitude) ||
    !Number.isFinite(longitude) ||
    latitude < 49.05 ||
    latitude > 49.35 ||
    longitude < 16.35 ||
    longitude > 16.85
  ) {
    return null;
  }
  const number = orientationNumber
    ? `${descriptiveNumber}/${orientationNumber}`
    : descriptiveNumber;
  return {
    sourceAddressId: cleanString(attributes.idadr || attributes.objectid),
    street,
    number,
    addressText: `${street} ${number}, Brno`,
    city,
    cityPart: cleanString(attributes.naz_cast),
    postalCode: cleanString(attributes.psc),
    latitude: Number(latitude.toFixed(7)),
    longitude: Number(longitude.toFixed(7))
  };
}

async function loadAddresses() {
  const idsPayload = await arcgisQuery({
    where: "naz_ul_a IS NOT NULL AND cis_d IS NOT NULL AND naz_obec = 'Brno'",
    returnIdsOnly: "true"
  });
  const objectIds = shuffled(idsPayload.objectIds || [], ADDRESS_SEED).slice(0, CANDIDATE_COUNT);
  if (objectIds.length < TARGET_COUNT) {
    throw new Error(`GIS Brno poskytlo jen ${objectIds.length} kandidátů.`);
  }

  const featureById = new Map();
  for (const objectIdChunk of chunks(objectIds, QUERY_CHUNK_SIZE)) {
    const payload = await arcgisQuery({
      objectIds: objectIdChunk.join(","),
      outFields: "objectid,idadr,naz_ul_a,cis_d,cis_o,naz_obec,naz_cast,psc",
      returnGeometry: "true",
      outSR: "4326"
    });
    for (const feature of payload.features || []) {
      featureById.set(Number(feature.attributes?.objectid), feature);
    }
  }

  const addresses = [];
  const uniqueAddresses = new Set();
  for (const objectId of objectIds) {
    const address = normalizedAddress(featureById.get(Number(objectId)));
    if (!address || !address.sourceAddressId) continue;
    const key = `${address.street}|${address.number}|${address.postalCode}`.toLocaleLowerCase("cs");
    if (uniqueAddresses.has(key)) continue;
    uniqueAddresses.add(key);
    addresses.push(address);
    if (addresses.length === TARGET_COUNT) break;
  }

  if (addresses.length !== TARGET_COUNT) {
    throw new Error(`Po validaci vzniklo jen ${addresses.length} z ${TARGET_COUNT} adres.`);
  }
  return addresses;
}

function outputSource(addresses) {
  const rows = addresses.map((address) => `  Object.freeze(${JSON.stringify(address)})`).join(",\n");
  return `// Generated by scripts/generate-collection-routes-brno-addresses.mjs.\n` +
    `// Source: ${ADDRESS_LAYER_QUERY_URL.replace(/\/query$/, "")}\n` +
    `// Deterministic seed: ${ADDRESS_SEED}\n\n` +
    `export const COLLECTION_ROUTES_TEST_BRNO_ADDRESS_SOURCE = ${JSON.stringify(ADDRESS_LAYER_QUERY_URL.replace(/\/query$/, ""))};\n` +
    `export const COLLECTION_ROUTES_TEST_BRNO_ADDRESS_SEED = ${ADDRESS_SEED};\n` +
    `export const COLLECTION_ROUTES_TEST_BRNO_ADDRESSES = Object.freeze([\n${rows}\n]);\n`;
}

const addresses = await loadAddresses();
await mkdir(path.dirname(OUTPUT_PATH), { recursive: true });
await writeFile(OUTPUT_PATH, outputSource(addresses), "utf8");
console.log(`Vygenerováno ${addresses.length} veřejných adresních bodů Brna: ${OUTPUT_PATH}`);
